        <h2>What is Diabetes</h2>
        <p>
            Image result for simple definition of diabeteswww.ethoshealth.com.au
            Diabetes mellitus (sometimes called "sugar diabetes") is a condition that occurs when the body can't use glucose (a type of sugar) normally. Glucose is the main source of energy for the body's cells. The levels of glucose in the blood are controlled by a hormone called insulin, which is made by the pancreas.
        </p>

        <h2>Diabetes Symptoms</h2>

        <h3>Common symptoms of diabetes:</h3>
        <ul>
            <li>Urinating often</li>
            <li>Feeling very thirsty</li>
            <li>Feeling very hungry - even though you are eating</li>
            <li>Extreme fatigue</li>
            <li>Blurry vision</li>
            <li>Cuts/bruises that are slow to heal</li>
            <li>Weight loss - even though you are eating more (type 1)</li>
            <li>Tingling, pain, or numbness in the hands/feet (type 2)</li>
        </ul>

    	<p>Early detection and treatment of diabetes can decrease the risk of developing the complications of diabetes.</p>
        
        <h3> Treatment</h3>
        <h4>Treatment for Common type diabetes includes:</h4> 
        <ul>
            <li>Healthy eating</li>
            <li>Regular exercise</li>
            <li>Possibly, diabetes medication or insulin therapy</li>
            <li>Blood sugar monitoring</li>
        </ul>

         <h3>Type 1 diabetes signs and symptoms can appear relatively suddenly and may include:</h3>
         <ul>
             <li>Increased thirst</li>
             <li>Frequent urination</li>
             <li>Bed-wetting in children who previously didn't wet the bed during the night</li>
             <li>Extreme hunger</li>
             <li>Unintended weight loss</li>
             <li>Irritability and other mood changes</li>
             <li>Fatigue and weakness</li>
             <li>Blurred vision</li>
         </ul>
        
        <h3> Treatment</h3>
        <h4>Treatment for type 1 diabetes includes:</h4>   
        <ul>
            <li>Taking insulin</li>
            <li>Carbohydrate, fat and protein counting</li>
            <li>Frequent blood sugar monitoring</li>
            <li>Eating healthy foods</li>
            <li>Exercising regularly and maintaining a healthy weight</li>
        </ul>

        <h3>Type 2 diabetes signs and symptoms may include:</h3>
        <ul>
            <li> Increased thirst</li>
            <li>Increased hunger (especially after eating)</li>
            <li>Dry mouth</li>
            <li>Frequent urination</li>
            <li>Unexplained weight loss (even though you are eating and feel hungry)</li>
            <li>Fatigue (weak, tired feeling)</li>
            <li>Blurred vision</li>
            <li>Headaches</li>
        </ul> 

        <h3> Treatment</h3>
        <h4>Treatment for type 2 diabetes includes:</h4> 
        <ul>
            <li>Healthy eating</li>
            <li>Regular exercise</li>
            <li>Possibly, diabetes medication or insulin therapy</li>
            <li>Blood sugar monitoring</li>
        </ul>

         <h3>Diabetes signs and symptoms for both man and woman may include:</h3>
         <ul>
             <li>Excessive thirst and hunger</li>
             <li>Frequent urination (from urinary tract infections or kidney problems)</li>
             <li>Weight loss or gain</li>
             <li>Fatigue</li>
             <li>Irritability</li>
             <li>Blurred vision</li>
             <li>Slow-healing wounds</li>
             <li>Nausea</li>
             <li>Skin infections</li>
             <li>Darkening of skin in areas of body creases (acanthosis nigricans)</li>
             <li>Breath odor that is fruity, sweet, or an acetone odor</li>
             <li>Tingling or numbness in the hands or feet</li>
         </ul>

        <h3> Treatment</h3>
        <h4>Treatment for both man and woman diabetes includes:</h4> 
        <ul>
            <li>Healthy eating</li>
            <li>Regular exercise</li>
            <li>Possibly, diabetes medication or insulin therapy</li>
            <li>Blood sugar monitoring</li>
        </ul>

