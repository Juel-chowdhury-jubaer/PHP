<section class="livingwithdiabetes">
    <h2>How to live with diabetes</h2>
    <p>It is common to feel overwhelmed, sad, or angry when you are living with diabetes. You may know the steps you should take to stay healthy, but have trouble sticking with your plan over time. This section has tips on how to cope with your diabetes, eat well, and be active.
    </p>
    <h3>Cope with your diabetes.</h3>
    <ul>
        <li>Stress can raise your blood sugar. Learn ways to lower your stress. Try deep breathing, gardening, taking a walk, meditating, working on your hobby, or listening to your favorite music.</li>
        <li>Ask for help if you feel down. A mental health counselor, support group, member of the clergy, friend, or family member who will listen to your concerns may help you feel better.</li>
    </ul>
    <h3>Eat well.</h3>
    <ul>
        <li>Make a diabetes meal plan with help from your health care team.</li>
        <li>Choose foods that are lower in calories, saturated fat, trans fat, sugar, and salt.</li>
        <li>Eat foods with more fiber, such as whole grain cereals, breads, crackers, rice, or pasta.</li>
        <li>Choose foods such as fruits, vegetables, whole grains, bread and cereals, and low-fat or skim milk and cheese.</li>
        <li>Drink water instead of juice and regular soda</li>
        <li>When eating a meal, fill half of your plate with fruits and vegetables, one quarter with a lean protein, such as beans, or chicken or turkey without the skin, and one quarter with a whole grain, such as brown rice or whole wheat pasta.</li>
    </ul>
    <h3>Be active.</h3>
    <ul>
        <li>Set a goal to be more active most days of the week. Start slow by taking 10 minute walks, 3 times a day.</li>
        <li>Twice a week, work to increase your muscle strength. Use stretch bands, do yoga, heavy gardening (digging and planting with tools), or try push-ups.</li>
        <li>Stay at or get to a healthy weight by using your meal plan and moving more.</li>
    </ul>
    <h3>Know what to do every day.</h3>
    <ul>
        <li>Take your medicines for diabetes and any other health problems even when you feel good. Ask your doctor if you need aspirin to prevent a heart attack or stroke. Tell your doctor if you cannot afford your medicines or if you have any side effects.</li>
        <li>Check your feet every day for cuts, blisters, red spots, and swelling. Call your health care team right away about any sores that do not go away.</li>
        <li>Brush your teeth and floss every day to keep your mouth, teeth, and gums healthy.</li>
        <li>Stop smoking. Ask for help to quit. Call 1-800-QUITNOW (1-800-784-8669).</li>
        <li>Keep track of your blood sugar. You may want to check it one or more times a day. Use the card at the back of this booklet to keep a record of your blood sugar numbers. Be sure to talk about it with your health care team.</li>
        <li>Check your blood pressure if your doctor advises and keep a record of it.</li>      
    </ul>
    <h3>Talk to your health care team.</h3>
    <ul>
        <li>check markAsk for a healthy meal plan.</li>
        <li>check markAsk about ways to be more active.</li>
        <li>Ask how and when to test your blood sugar and how to use the results to manage your diabetes.</li>
        <li>Use these tips to help with your self-care.</li>
        <li>Discuss how your diabetes plan is working for you each time you visit your health care team.</li>
    </ul>
